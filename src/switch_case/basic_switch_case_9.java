package switch_case;

public class basic_switch_case_9 {

    public static void main(String[] args) {

        int a=-1;
        switch(a){
            case 1:
                System.out.println("10");
                break;
            case -1:
                System.out.println("11");
                break;
            default:
                System.out.println("0");
        }
    }
}
